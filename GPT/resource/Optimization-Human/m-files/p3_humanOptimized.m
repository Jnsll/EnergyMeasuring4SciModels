function a = helpFun(N, b, str, a, offset)
for i = 1:N
    if b(i) < 10
        imageName = ['00',int2str(b(i)),str,'.bmp'];
    elseif b(i) < 100
        imageName = ['0',int2str(b(i)),str,'.bmp'];
    else
        imageName = [int2str(b(i)),str,'.bmp'];
    end
    a(:,:,i + offset) = imread(imageName);
end
end